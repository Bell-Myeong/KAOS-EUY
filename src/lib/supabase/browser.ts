import { createBrowserClient } from '@supabase/ssr';
import type { SupabaseClient } from '@supabase/supabase-js';
import type { Database } from './database.types';

declare global {
  // eslint-disable-next-line no-var
  var __kaosEuySupabaseBrowserClient: SupabaseClient<Database> | undefined;
}

export function getSupabaseBrowserClient() {
  const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const supabaseAnonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

  if (!supabaseUrl || !supabaseAnonKey) {
    throw new Error(
      'Missing NEXT_PUBLIC_SUPABASE_URL or NEXT_PUBLIC_SUPABASE_ANON_KEY'
    );
  }

  if (!globalThis.__kaosEuySupabaseBrowserClient) {
    globalThis.__kaosEuySupabaseBrowserClient = createBrowserClient<Database>(
      supabaseUrl,
      supabaseAnonKey
    );
  }

  return globalThis.__kaosEuySupabaseBrowserClient;
}

